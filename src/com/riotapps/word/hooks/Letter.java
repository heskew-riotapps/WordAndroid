package com.riotapps.word.hooks;

public class Letter {

	private String C;
	private int V;
	
	public String getCharacter() {
		return C;
	}
	public void setCharacter(String character) {
		this.C = character;
	}
	public int getValue() {
		return V;
	}
	public void setValue(int value) {
		this.V = value;
	}
	
	
}
